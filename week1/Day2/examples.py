"""Arithmetic Operator: + (Addition)
Python"""

x = 5
y = 3
print(x + y)  # Output: 8
# Comparison Operator: == (Equal to) #
x = 5
y = 5
print(x == y)  
# Output: True

#Logical Operator: and (Logical and) #

x = 5
y = 3
print(x > y and y > 0)  
# Output: True

# Assignment Operator: += (Addition assignment)

x = 5
y = 3
x += y
print(x)  # Output: 8

# Membership Operator: in (Membership test) #
fruits = ['apple', 'banana', 'cherry']
print('banana' in fruits)  
# Output: True

# Identity Operator: is (Identity test)

x = [1, 2, 3]
y = [1, 2, 3]
print(x is y)  # Output: False

# Bitwise Operator: & (Bitwise and)
x = 5   # Binary: 101
y = 3   # Binary: 011
print(x & y)  
# Output: 1 (Binary: 001)